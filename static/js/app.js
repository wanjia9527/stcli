async function autoStartKeepalive() {
  try {
    var settings = await api().get_settings();
    if (settings.keepalive_autostart) {
      var result = await api().keepalive_start();
      if (result.ok) {
        document.getElementById('ka-status-dot').className = 'status-dot running';
        document.getElementById('ka-status-text').textContent = '运行中';
        document.getElementById('ka-start-btn').disabled = true;
        document.getElementById('ka-stop-btn').disabled = false;
        document.getElementById('ka-check-btn').disabled = false;
      }
    }
  } catch (e) {}
}

function initModalActions() {
  document.getElementById('modal-body').addEventListener('click', function(e) {
    var btn = e.target.closest('.modal-action');
    if (!btn) return;
    var action = btn.getAttribute('data-action');
    var id = btn.getAttribute('data-id');
    if (!action) return;

    switch (action) {
      case 'copy-link': closeModal(); copyFileLink(id, _currentModalIsCollection); break;
      case 'copy-dl-link': closeModal(); copyDownloadLink(id, _currentModalIsCollection); break;
      case 'qr-code': showQrCode(id, _currentModalIsCollection); break;
      case 'open-browser': openInBrowser(id, _currentModalIsCollection); break;
      case 'add-keepalive': closeModal(); addToKeepalive(id); break;
      case 'set-expiry': showSetExpiry(id); break;
      case 'set-password': showSetPassword(id); break;
      case 'set-downloads': showSetDownloads(id); break;
      case 'delete-file': deleteSingleFile(id); break;
      case 'confirm-expiry': doSetExpiry(id); break;
      case 'confirm-password': doSetPassword(id); break;
      case 'confirm-downloads': doSetDownloads(id); break;
      case 'confirm-create-coll': doCreateCollection(); break;
      case 'confirm-add-ka': doAddKeepalive(); break;
      case 'confirm-add-ka-token': doAddKeepaliveWithToken(id); break;
      case 'confirm-batch-expiry': doBatchSetExpiry(); break;
      case 'confirm-batch-password': doBatchSetPassword(); break;
    }
  });

  // File list event delegation
  document.getElementById('file-list').addEventListener('click', function(e) {
    // Handle file action buttons
    var actionBtn = e.target.closest('.file-action');
    if (actionBtn) {
      e.stopPropagation();
      var action = actionBtn.getAttribute('data-action');
      var fileId = actionBtn.getAttribute('data-file-id');
      // Look up is_collection from allFiles
      var isColl = false;
      for (var fi = 0; fi < allFiles.length; fi++) {
        if (allFiles[fi].id === fileId) { isColl = allFiles[fi].is_collection; break; }
      }
      if (action === 'download') downloadFile(fileId, isColl);
      else if (action === 'copy-link') copyFileLink(fileId, isColl);
      else if (action === 'copy-dl-link') copyDownloadLink(fileId, isColl);
      else if (action === 'qr-code') showQrCode(fileId, isColl);
      else if (action === 'open-browser') openInBrowser(fileId, isColl);
      else if (action === 'add-keepalive') addToKeepalive(fileId);
      else if (action === 'more') showFileMenu(fileId);
      else if (action === 'set-expiry') showSetExpiry(fileId);
      else if (action === 'set-password') showSetPassword(fileId);
      else if (action === 'set-downloads') showSetDownloads(fileId);
      else if (action === 'delete-file') deleteSingleFile(fileId);
      return;
    }
    // Handle card checkbox
    var cb = e.target.closest('.file-checkbox');
    if (cb) {
      e.stopPropagation();
      toggleFileSelect(cb.getAttribute('data-file-id'), cb.checked);
      return;
    }
    // Handle select-all checkbox
    if (e.target.id === 'select-all-cb') {
      toggleSelectAll(e.target.checked);
      return;
    }
    // Handle card click (toggle select)
    var card = e.target.closest('.card[data-file-id]');
    if (card) {
      var fid = card.getAttribute('data-file-id');
      var isSelected = selectedFiles.has(fid);
      toggleFileSelect(fid, !isSelected);
    }
  });

  // File mode option cards (settings page)
  document.addEventListener('click', function(e) {
    var card = e.target.closest('[data-file-mode]');
    if (card) {
      switchFileMode(card.getAttribute('data-file-mode'));
    }
  });
}

async function updateStatusBar() {
  try {
    const auth = await api().get_auth_mode();
    const quota = await api().get_quota();
    const authEl = document.getElementById('status-auth');
    const quotaEl = document.getElementById('status-quota');
    if (auth.mode === 'token') {
      authEl.textContent = '已登录: ' + (auth.username || '');
    } else {
      authEl.textContent = '匿名';
    }
    if (quota.authenticated) {
      quotaEl.textContent = '配额: 无限制';
    } else {
      quotaEl.textContent = '配额: ' + quota.used_gb.toFixed(1) + '/' + quota.limit_gb.toFixed(0) + ' GB';
    }
  } catch (e) {
    document.getElementById('status-auth').textContent = '离线';
  }
}
